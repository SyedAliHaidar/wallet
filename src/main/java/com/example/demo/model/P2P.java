package com.example.demo.model;

public class P2P {
    Integer debitorNo;
    Integer creditorNo;
    Integer amount;

    public Integer getCreditorNo() {
        return creditorNo;
    }

    public void setCreditorNo(Integer creditorNo) {
        this.creditorNo = creditorNo;
    }

    public Integer getAmount() {
        return amount;
    }

    public void setAmount(Integer amount) {
        this.amount = amount;
    }

    public Integer getDebitorNo() {
        return debitorNo;
    }

    public void setDebitorNo(Integer debitorNo) {
        this.debitorNo = debitorNo;
    }
}
